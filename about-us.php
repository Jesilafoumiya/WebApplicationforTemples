      $("#submit").click(function(){
          var this_btn = $(this);
          var firstname = $("#firstname").val().trim();
          var lastname = $("#lastname").val().trim();
          var subject = $("#subject").val().trim();
          var email = $("#email").val().trim();
          var message = $("#message").val().trim();
          
          if(firstname == "")
          {
            $(".response").html("Please, Enter Firstname!");
          }
          else if(lastname == "")
          {
            $(".response").html("Please, Enter Lastname!");
          }
          else if(email == "")
          {
            $(".response").html("Please, Enter Email!");
          }
          else if(message == "")
          {
            $(".response").html("Please, Enter Message!");
          }
          else
          {
            $("#submit").attr('disabled', true).html('Sending...');

            var fd = new FormData();

              fd.append("firstname", firstname);
              fd.append("lastname", lastname);
              fd.append("subject", subject);
              fd.append("email", email);
              fd.append("message", message);

              $.ajax({
                url:'ajax/contact.php',
                processData: false,
                contentType: false,
                data: fd,
                type: 'post',
                success: function(response)
                {
                  console.log(response);
                  if(response == "Success")
                  {
                    $("#submit").attr('disabled', false).html('Mail Sended!, Thanks for Contact Us');

                  }
                  else
                  {
                    $("#submit").attr('disabled', true).html('Mail Not Send, Please Contact via Call!');
                  }
                }
              });
          }
      });
    });
